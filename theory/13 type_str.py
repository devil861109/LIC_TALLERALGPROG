# La función de tipo le permite averiguar el tipo de datos de un dato
# Para usarlo, escribe lo que quieras saber el tipo de datos entre paréntesis

ex_1 = False
ex_2 = 29
ex_3 = 4.1352

print(type(ex_1))
print(type(ex_2))
print(type(ex_3))

# La función de cadena convierte todo lo que se pone entre paréntesis en una
# cadena
# Para usarlo, escribe str paréntesis, luego lo que quieras que se convierta
# en una cadena dentro de esos paréntesis

print(str(ex_1))
print(str(ex_2))
print(str(ex_3))

print(type(str(ex_1)))
print(type(str(ex_2)))
print(type(str(ex_3)))