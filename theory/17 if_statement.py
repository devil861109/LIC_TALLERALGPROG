# Sintaxis IF
if True:
    "Do some stuff"

# Hay cuatro cosas que toda declaración if debe tener.
# La palabra if, una condición, que si se evalúa como verdadera hará que la
# declaración if ejecute su código, dos puntos
# luego codifique para que la declaración if se ejecute si su condición se
# evalúa como verdadera

veg = input("Dame el nombre de un vegetal")
if (veg == "tomate"):
    print("Es un tomate!!")