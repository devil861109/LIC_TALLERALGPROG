# Reglas para variables
# Puede utilizar cualquier combinación de letras mayúsculas y minúsculas
# Cuando crea una variable, debe asignar un nombre que le ayude a usted y a
# otros a comprender qué hace la variable y su utilidad

# Una variable no puede tener un número al principio de su nombre
# 4callingbirds = 10

# Una variable no puede tener espacios entre ellas
# not okay = 8

# No puede utilizar caracteres especiales como $. ! ? al nombrar una variable
# $please.avoid!?

# Hay dos formas aceptables de crear nombres de variables en Python
# camelCase = 100
# snake_case = 200

# También puede utilizar números en el nombre de una variable siempre que
# no se utilice un número como primer carácter del nombre
# numb3rsOk4yAfter1st = 900